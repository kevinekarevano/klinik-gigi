import mongoose from "mongoose";

const serviceSchema = new mongoose.Schema(
  {
    image: {
      type: String,
      required: true,
    },
    title: {
      type: String,
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const serviceModel = mongoose.model("Service", serviceSchema);

export default serviceModel;
